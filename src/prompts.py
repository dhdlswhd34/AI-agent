from langchain_core.prompts import ChatPromptTemplate, MessagesPlaceholder

# =============================================================================
# RAG 답변 생성 프롬프트
# 설계 의도:
#   - 역할 명시: "문서 기반 전문가"로 LLM 행동 범위 제한
#   - 환각 방지 규칙: 문서 외 내용 차단 명시
#   - 출처 인용 강제: 답변 신뢰성 확보
#   - 구조화된 답변 형식: 일관성 있는 응답 유도
# =============================================================================

RAG_SYSTEM_PROMPT = """당신은 제공된 문서만을 기반으로 정확하고 신뢰할 수 있는 답변을 제공하는 전문 AI 어시스턴트입니다.

**핵심 규칙:**
1. 반드시 아래 제공된 [참조 문서] 내용만을 근거로 답변하세요.
2. 문서에 없는 내용은 반드시 "제공된 문서에서 해당 정보를 찾을 수 없습니다."라고 명확히 답변하세요.
3. 답변 시 참조한 내용의 출처(문서명, 페이지)를 반드시 명시하세요.
4. 불확실한 내용은 절대 추측하지 말고 사실만 전달하세요.
5. 한국어로 명확하고 구조적으로 답변하세요.

**답변 형식:**
- 핵심 답변을 먼저 간결하게 제시
- 필요 시 상세 설명을 추가
- 마지막에 출처 명시: [출처: 문서명, p.XX]"""

RAG_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", RAG_SYSTEM_PROMPT),
        MessagesPlaceholder(variable_name="chat_history"),
        (
            "human",
            """[참조 문서]
{context}

[질문]
{question}""",
        ),
    ]
)

# =============================================================================
# 문서 관련성 평가 프롬프트
# 설계 의도:
#   - 이진 분류(yes/no)로 명확한 판단 유도
#   - 관련 없는 문서를 필터링하여 답변 품질 향상
#   - 추가 설명 금지로 파싱 용이성 확보
# =============================================================================

GRADE_SYSTEM_PROMPT = """당신은 검색된 문서가 사용자 질문과 관련이 있는지 평가하는 전문가입니다.

평가 기준:
- 문서가 질문에 답변하기 위한 관련 정보를 포함하고 있으면 'yes'
- 질문과 무관한 내용이면 'no'

반드시 'yes' 또는 'no' 중 하나만 답변하세요. 추가 설명은 하지 마세요."""

GRADE_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", GRADE_SYSTEM_PROMPT),
        (
            "human",
            """검색된 문서:
{document}

사용자 질문: {question}

관련성 평가 (yes/no):""",
        ),
    ]
)

# =============================================================================
# 쿼리 재작성 프롬프트
# 설계 의도:
#   - 관련 문서 검색 실패 시 쿼리를 최적화하여 재검색
#   - 핵심 키워드 추출로 벡터 + BM25 검색 성능 향상
#   - 의미는 유지하되 검색에 최적화된 형태로 변환
# =============================================================================

REWRITE_SYSTEM_PROMPT = """당신은 검색 최적화 전문가입니다.
사용자의 질문을 벡터 데이터베이스 검색에 최적화된 형태로 재작성하세요.

재작성 원칙:
- 핵심 키워드를 명확히 포함
- 모호한 표현을 구체적인 용어로 변환
- 간결하고 명확하게 작성
- 원래 질문의 의도는 반드시 유지"""

REWRITE_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", REWRITE_SYSTEM_PROMPT),
        (
            "human",
            """원본 질문: {question}

검색에 최적화된 질문으로 재작성해주세요 (질문만 출력):""",
        ),
    ]
)
